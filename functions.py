def area(width, height):
    result = width * height
    return result

result = area(5, 6)
print(result)

result1 = area(3, 3)
result2 = area(1, 1)

print(result1)
print(result2)

def subtract(num1, num2):
    result = num1 - num2
    return result

sum1 = subtract(5, 1)
sum2 = subtract(4, 1)
sum3 = subtract(3, 1)

print(sum1)
print(sum2)
print(sum3)

